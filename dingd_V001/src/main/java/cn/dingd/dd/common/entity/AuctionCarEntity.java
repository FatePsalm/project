package cn.dingd.dd.common.entity;

import java.io.Serializable;

/**
* @author 作者 Name:CaoGang
* @version 创建时间：2017年12月6日 下午4:43:21
* 类说明
* 拍卖场次和车辆关系
*/
public class AuctionCarEntity  implements Serializable{

	/**
	 * 系列号
	 */
	private static final long serialVersionUID = -2959186748146955737L;
	//id 
	private int id;
	//车辆id
	private int carId;
	//场次id
	private int auctionId;
	//场次车辆排序
	private int sort;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getCarId() {
		return carId;
	}
	public void setCarId(int carId) {
		this.carId = carId;
	}
	public int getAuctionId() {
		return auctionId;
	}
	public void setAuctionId(int auctionId) {
		this.auctionId = auctionId;
	}
	public int getSort() {
		return sort;
	}
	public void setSort(int sort) {
		this.sort = sort;
	}
	@Override
	public String toString() {
		return "AuctionCarEntity [id=" + id + ", carId=" + carId + ", auctionId=" + auctionId + ", sort=" + sort + "]";
	}
	
}
