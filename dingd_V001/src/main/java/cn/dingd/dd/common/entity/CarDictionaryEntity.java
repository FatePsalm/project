package cn.dingd.dd.common.entity;

import java.io.Serializable;

/**
 * @author 作者 Name:CaoGang
 * @version 创建时间：2017年11月4日 上午11:25:52 类说明 车辆数据字典
 */
public class CarDictionaryEntity implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = -861939671902245116L;

	/** ID */
	private Integer id;
	/** 文本值 */
	private String dictName;
	/** 文本code */
	private Integer dictValue;
	/** 排列顺序 */
	private Integer dictOrder;
	/** 序号(编码） */
	private  String code;
	/** 描述 */
	private String dictDesc;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getDictName() {
		return dictName;
	}
	public void setDictName(String dictName) {
		this.dictName = dictName;
	}
	public Integer getDictValue() {
		return dictValue;
	}
	public void setDictValue(Integer dictValue) {
		this.dictValue = dictValue;
	}
	public Integer getDictOrder() {
		return dictOrder;
	}
	public void setDictOrder(Integer dictOrder) {
		this.dictOrder = dictOrder;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getDictDesc() {
		return dictDesc;
	}
	public void setDictDesc(String dictDesc) {
		this.dictDesc = dictDesc;
	}
	@Override
	public String toString() {
		return "CarDictionaryEntity [id=" + id + ", dictName=" + dictName + ", dictValue=" + dictValue + ", dictOrder="
				+ dictOrder + ", code=" + code + ", dictDesc=" + dictDesc + "]";
	}
	

	

}
